-- Q10. Find all movies released after 2015 where revenue is greater than budget.
-- (Using same normalization approach as above.)

SELECT
  m.movie_id, m.title, m.release_year,
  f.currency,
  (CASE WHEN LOWER(f.unit) LIKE '%billion%' THEN f.budget * 1000 ELSE f.budget END) AS budget_in_millions,
  (CASE WHEN LOWER(f.unit) LIKE '%billion%' THEN f.revenue * 1000 ELSE f.revenue END) AS revenue_in_millions
FROM movies m
JOIN financials f ON m.movie_id = f.movie_id
WHERE m.release_year > 2015
  AND (CASE WHEN LOWER(f.unit) LIKE '%billion%' THEN f.revenue * 1000 ELSE f.revenue END)
      > (CASE WHEN LOWER(f.unit) LIKE '%billion%' THEN f.budget * 1000 ELSE f.budget END)
ORDER BY revenue_in_millions - budget_in_millions DESC;




