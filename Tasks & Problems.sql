    movie_id,
    currency,
    unit,
    CASE WHEN LOWER(unit) LIKE '%billion%' THEN budget * 1000 ELSE budget END AS budget_m,
    CASE WHEN LOWER(unit) LIKE '%billion%' THEN revenue * 1000 ELSE revenue END AS revenue_m
  FROM financials
) f
